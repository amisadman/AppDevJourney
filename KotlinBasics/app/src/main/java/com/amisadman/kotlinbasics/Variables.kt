package com.amisadman.kotlinbasics

fun main(){
    var myAge : Byte = 21
    myAge = 127
    println(myAge)

    val birthYear : Short = 2003
    println(birthYear)

    var pi = 3.1416
    pi = 3.14159865
    println(pi)

    var hisAge : UByte = 155u
    print(hisAge)

    var isRaining : Boolean = false
    println(isRaining)
    isRaining = true
    println(isRaining)

}